package ru.ds.pna;

public class Article {

	private String title;
	private String picture;

	public void setTitle(String title) {
		this.title = title;
	}

	public void setPicture(String picture) {
		this.picture = picture;
	}

	public String getTitle() {
		return title;
	}

	public String getPicture() {
		return picture;
	}
}